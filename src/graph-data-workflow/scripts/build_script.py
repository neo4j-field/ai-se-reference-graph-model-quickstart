def build_script(config_path, output_path):
    """Read config, embed it as a literal Python dict, write the runnable script."""
    with open(config_path) as f:
        config = json.load(f)

    # Embed the config as a Python literal. json.dumps then a simple
    # bool/null fix-up is more robust than building a repr of nested dicts.
    config_repr = json.dumps(config, indent=4)
    # Convert JSON literals to Python literals
    config_repr = (config_repr
        .replace(": true", ": True")
        .replace(": false", ": False")
        .replace(": null", ": None"))

    # Simple string replacement (not str.format) so that braces inside the
    # template — used by f-strings, dict literals, etc. — pass through
    # untouched. We use a sentinel that can't appear in legitimate Python.
    rendered = GENERATE_PY_TEMPLATE.replace("__EMBEDDED_CONFIG__", config_repr)

    with open(output_path, "w") as f:
        f.write(rendered)
    os.chmod(output_path, 0o755)


def main():
    ap = argparse.ArgumentParser(
        description="Emit a standalone generate.py from a run config.",
    )
    ap.add_argument("config", help="Path to run_config.json")
    ap.add_argument("output", help="Path where generate.py should be written")
    args = ap.parse_args()

    build_script(args.config, args.output)
    print(f"Wrote {args.output}", file=sys.stderr)


