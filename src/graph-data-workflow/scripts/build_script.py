_sequence_counters = defaultdict(int)


def _parse_relative_date(s):
    """Parse 'today', 'today-3y', 'today+30d' or an ISO date string."""
    if not isinstance(s, str):
        return s
    s = s.strip()
    if s == "today":
        return datetime.now()
    m = re.match(r"^today([+-])(\\d+)([dwmy])$", s)
    if m:
        sign, n, unit = m.group(1), int(m.group(2)), m.group(3)
        if sign == "-":
            n = -n
        if unit == "d":
            return datetime.now() + timedelta(days=n)
        if unit == "w":
            return datetime.now() + timedelta(weeks=n)
        if unit == "m":
            return datetime.now() + timedelta(days=30 * n)
        if unit == "y":
            return datetime.now() + timedelta(days=365 * n)
    return datetime.fromisoformat(s)


def make_generator(spec):
    """Return a zero-arg callable that produces values according to the spec.

    Supported types:
        faker        — calls fake.<method>(**args)
        categorical  — random.choices(values, weights)
        regex        — fake.bothify-style pattern via faker
        lognormal    — random.lognormvariate, optional min/max/round_to
        normal       — random.gauss, optional min/max/round_to
        uniform      — random.uniform or randint, optional round_to
        sequence     — counter with prefix/pad
        datetime     — random datetime in [start, end]
        derived      — value computed from another property; resolved later
        constant     — fixed value
    """
    t = spec.get("type")

    if t == "faker":
        method = spec["method"]
        args = spec.get("args", {})
        fn = getattr(fake, method)
        return lambda: fn(**args)

    if t == "categorical":
        values = spec["values"]
        weights = spec.get("weights")
        if weights:
            return lambda: random.choices(values, weights=weights, k=1)[0]
        return lambda: random.choice(values)

    if t == "regex":
        pattern = spec["pattern"]
        # Faker's hexify/bothify cover common cases. For full regex support
        # we'd need rstr or similar; here we handle the common patterns
        # used in our packs (alphanumeric ranges, fixed strings).
        return lambda: _regex_fill(pattern)

    if t == "lognormal":
        mu = spec.get("mu", 0.0)
        sigma = spec.get("sigma", 1.0)
        lo = spec.get("min")
        hi = spec.get("max")
        rd = spec.get("round_to")
        def _gen():
            v = random.lognormvariate(mu, sigma)
            if lo is not None: v = max(lo, v)
            if hi is not None: v = min(hi, v)
            if rd is not None: v = round(v, rd)
            return v
        return _gen

    if t == "normal":
        mean = spec.get("mean", 0.0)
        std = spec.get("std", 1.0)
        lo = spec.get("min")
        hi = spec.get("max")
        rd = spec.get("round_to")
        def _gen():
            v = random.gauss(mean, std)
            if lo is not None: v = max(lo, v)
            if hi is not None: v = min(hi, v)
            if rd is not None: v = round(v, rd)
            return v
        return _gen

    if t == "uniform":
        lo = spec["min"]
        hi = spec["max"]
        if spec.get("integer"):
            return lambda: random.randint(int(lo), int(hi))
        rd = spec.get("round_to")
        if rd is not None:
            return lambda: round(random.uniform(lo, hi), rd)
        return lambda: random.uniform(lo, hi)

    if t == "sequence":
        prefix = spec.get("prefix", "")
        start = spec.get("start", 1)
        pad = spec.get("pad_width")
        key = id(spec)
        _sequence_counters[key] = start - 1
        def _gen():
            _sequence_counters[key] += 1
            n = _sequence_counters[key]
            if pad:
                return f"{prefix}{n:0{pad}d}"
            return f"{prefix}{n}"
        return _gen

    if t == "datetime":
        start = _parse_relative_date(spec.get("start", "today-1y"))
        end = _parse_relative_date(spec.get("end", "today"))
        fmt = spec.get("format", "iso")
        def _gen():
            delta = (end - start).total_seconds()
            offset = random.uniform(0, delta)
            d = start + timedelta(seconds=offset)
            if fmt == "date":
                return d.date().isoformat()
            if fmt == "epoch_seconds":
                return int(d.timestamp())
            if fmt == "epoch_millis":
                return int(d.timestamp() * 1000)
            return d.isoformat()
        return _gen

    if t == "constant":
        return lambda: spec["value"]

    if t == "derived":
        # Marker — resolved during row construction (see _generate_node_row).
        return None

    # Unknown type — fall back to a safe word generator.
    return lambda: fake.word()


def _regex_fill(pattern):
    """Best-effort: handle [A-Z]{n}, [0-9]{n}, [A-F0-9]{n}, and literal text."""
    out = []
    i = 0
    while i < len(pattern):
        c = pattern[i]
        if c == "[":
            close = pattern.find("]", i)
            if close == -1:
                out.append(c); i += 1; continue
            charset = pattern[i+1:close]
            chars = _expand_charset(charset)
            i = close + 1
            # Check for {n}
            if i < len(pattern) and pattern[i] == "{":
                rclose = pattern.find("}", i)
                if rclose != -1:
                    n_str = pattern[i+1:rclose]
                    if "," in n_str:
                        lo, hi = n_str.split(",")
                        n = random.randint(int(lo), int(hi))
                    else:
                        n = int(n_str)
                    out.append("".join(random.choice(chars) for _ in range(n)))
                    i = rclose + 1
                    continue
            out.append(random.choice(chars))
        else:
            out.append(c); i += 1
    return "".join(out)


def _expand_charset(spec):
    chars = []
    i = 0
    while i < len(spec):
        if i + 2 < len(spec) and spec[i+1] == "-":
            for c in range(ord(spec[i]), ord(spec[i+2]) + 1):
                chars.append(chr(c))
            i += 3
        else:
            chars.append(spec[i]); i += 1
    return chars


def resolve_derived(label, prop_name, row, all_props):
    """Compute a derived property from already-generated row values."""
    spec = all_props.get(f"{label}.{prop_name}", {})
    src = spec.get("from", "")
    transform = spec.get("transform", "")
    src_label, _, src_prop = src.partition(".")
    src_val = row.get(src_prop, "") if src_label == label else ""
    if transform == "domain_from_email":
        return src_val.split("@", 1)[1] if "@" in src_val else ""
    if transform == "email_from_name":
        slug = re.sub(r"[^a-z]+", ".", str(src_val).lower()).strip(".")
        return f"{slug}@example.com"
    return src_val


