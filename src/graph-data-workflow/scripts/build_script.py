def inject_patterns(schema, id_index, output_dir):
    """Plant patterns and return the ground-truth manifest."""
    patterns = CONFIG.get("patterns", [])
    if not patterns:
        return None

    manifest = {"patterns": []}

    for pat in patterns:
        ptype = pat.get("type")
        if ptype == "cyclic":
            entries = _inject_cyclic(pat, schema, id_index, output_dir)
        elif ptype == "shared_attr":
            entries = _inject_shared_attr(pat, schema, id_index, output_dir)
        elif ptype == "hub":
            entries = _inject_hub(pat, schema, id_index, output_dir)
        else:
            print(f"  WARN: unknown pattern type {ptype!r}, skipping", file=sys.stderr)
            continue
        manifest["patterns"].append({"type": ptype, "spec": pat, "instances": entries})
        print(f"  planted {len(entries)} {ptype} patterns", file=sys.stderr)

    out_path = os.path.join(output_dir, "injected_patterns.json")
    with open(out_path, "w") as fh:
        json.dump(manifest, fh, indent=2)
    return out_path


def _append_rel_rows(output_dir, rel_type, rows):
    out_path = os.path.join(output_dir, f"rels_{rel_type}.csv")
    file_exists = os.path.exists(out_path)
    with open(out_path, "a", newline="") as fh:
        writer = csv.writer(fh)
        if not file_exists:
            writer.writerow(["_from_id", "_to_id"])
        for r in rows:
            writer.writerow(r)


def _inject_cyclic(pat, schema, id_index, output_dir):
    """A→B→C→...→A rings on a single node label via a self-rel."""
    rel_type = pat.get("rel")
    node_label = pat.get("node")
    count = pat.get("count", 50)
    rs = pat.get("ring_size", [3, 5])
    ids = id_index.get(node_label, [])
    if len(ids) < max(rs):
        return []

    instances = []
    appended = []
    for _ in range(count):
        size = random.randint(rs[0], rs[1])
        ring = random.sample(ids, size)
        for i in range(size):
            appended.append([ring[i], ring[(i + 1) % size]])
        instances.append({"ring": ring})
    _append_rel_rows(output_dir, rel_type, appended)
    return instances


def _inject_shared_attr(pat, schema, id_index, output_dir):
    """N members all linked to one shared hub (e.g. shared device cluster)."""
    rel_type = pat.get("rel")
    hub_label = pat.get("hub")
    member_label = pat.get("member")
    count = pat.get("count", 100)
    cs = pat.get("cluster_size", [5, 10])
    hub_ids = id_index.get(hub_label, [])
    member_ids = id_index.get(member_label, [])
    if not hub_ids or not member_ids:
        return []

    instances = []
    appended = []
    for _ in range(count):
        size = random.randint(cs[0], cs[1])
        if len(member_ids) < size:
            continue
        hub = random.choice(hub_ids)
        members = random.sample(member_ids, size)
        for m in members:
            appended.append([hub, m])
        instances.append({"hub": hub, "members": members})
    _append_rel_rows(output_dir, rel_type, appended)
    return instances


def _inject_hub(pat, schema, id_index, output_dir):
    """A few nodes with abnormally high out-degree."""
    rel_type = pat.get("rel")
    count = pat.get("count", 5)
    fanout = pat.get("fanout", 200)
    schema_rels = {r["type"]: r for r in schema.get("relationships", [])}
    if rel_type not in schema_rels:
        return []
    nodes_by_id = {n["id"]: n for n in schema.get("nodes", [])}
    rel_def = schema_rels[rel_type]
    from_lbl = primary_label(nodes_by_id[rel_def["fromId"]])
    to_lbl = primary_label(nodes_by_id[rel_def["toId"]])
    from_ids = id_index.get(from_lbl, [])
    to_ids = id_index.get(to_lbl, [])
    if not from_ids or not to_ids:
        return []

    instances = []
    appended = []
    for _ in range(count):
        hub = random.choice(from_ids)
        targets = random.sample(to_ids, min(fanout, len(to_ids)))
        for t in targets:
            appended.append([hub, t])
        instances.append({"hub": hub, "fanout": len(targets)})
