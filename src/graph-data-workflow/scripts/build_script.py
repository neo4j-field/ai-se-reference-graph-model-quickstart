def generate_nodes(schema, counts, output_dir):
    """Generate one CSV per node label. Returns {label: [list of _id values]}.

    If multiple node defs in the schema share the same primary label
    (e.g. ["Account", "Internal"] and ["Account", "External"] both have
    primary label "Account"), they are treated as ONE logical label —
    the union of their properties is used and counts[label] rows are
    written once.
    """
    id_index = {}
    prop_gens = CONFIG.get("property_generators", {})

    # Group node defs by primary label and union their properties.
    grouped = {}  # label -> {prop_name: prop_type}
    for node_def in schema.get("nodes", []):
        label = primary_label(node_def)
        merged = grouped.setdefault(label, {})
        for p, t in node_def.get("properties", {}).items():
            # First-seen type wins; later defs are idempotent for matching props.
            merged.setdefault(p, t)

    for label, properties in grouped.items():
        n = counts.get(label, 0)
        prop_names = list(properties.keys())

        # Compile generators
        generators = {}
        for p in prop_names:
            full_key = f"{label}.{p}"
            if full_key in prop_gens:
                generators[p] = make_generator(prop_gens[full_key])
            else:
                generators[p] = _default_generator_for(p, properties[p])

        out_path = os.path.join(output_dir, f"nodes_{label}.csv")
        ids = []
        with open(out_path, "w", newline="") as fh:
            writer = csv.writer(fh)
            writer.writerow(["_id"] + prop_names)
            for i in range(n):
                row = {}
                # First pass: non-derived
                for p in prop_names:
                    spec = prop_gens.get(f"{label}.{p}", {})
                    if spec.get("type") == "derived":
                        continue
                    g = generators[p]
                    row[p] = g() if g is not None else ""
                # Second pass: derived
                for p in prop_names:
                    spec = prop_gens.get(f"{label}.{p}", {})
                    if spec.get("type") == "derived":
                        row[p] = resolve_derived(label, p, row, prop_gens)
                _id = f"{label.lower()}-{i}"
                ids.append(_id)
                writer.writerow([_id] + [row.get(p, "") for p in prop_names])

        id_index[label] = ids
        print(f"  wrote {out_path} ({n:,} rows)", file=sys.stderr)

    return id_index


def _default_generator_for(prop_name, prop_type):
    """Tiny fallback when a property has no generator in the config."""
    name = prop_name.lower()
    if "email" in name:
        return lambda: fake.email()
    if "phone" in name:
        return lambda: fake.phone_number()
    if "name" in name:
        return lambda: fake.name()
    if name in ("id", "uuid") or name.endswith("id"):
        return lambda: fake.uuid4()
    if "date" in name or "time" in name:
        return lambda: fake.date_time_this_decade().isoformat()
    if "amount" in name or "price" in name:
        return lambda: round(random.lognormvariate(4.0, 1.2), 2)

    pt = (prop_type or "string").lower()
    if pt in ("int", "integer", "long"):
        return lambda: random.randint(1, 10000)
    if pt in ("float", "double", "number"):
        return lambda: round(random.uniform(0.0, 1000.0), 2)
    if pt in ("boolean", "bool"):
        return lambda: random.choice([True, False])
    if pt in ("date",):
        return lambda: fake.date_this_decade().isoformat()
    if pt in ("datetime", "timestamp"):
        return lambda: fake.date_time_this_decade().isoformat()

    return lambda: fake.word()


def generate_rels(schema, id_index, output_dir):
    """Generate one CSV per relationship type. Returns {rel_type: count}.

    If multiple rel defs in the schema share the same rel-type but
    connect different label pairs (e.g. LOCATED_IN going Address→Country
    AND IP→Location), all such defs append to the same CSV. The header
    is written once on first encounter.
    """
    rel_counts = {}
    nodes_by_id = {n["id"]: n for n in schema.get("nodes", [])}
    rel_props = CONFIG.get("rel_property_generators", {})
    cardinality = CONFIG.get("cardinality", {})

    # Track which rel-type CSVs are already opened so we don't overwrite headers.
    opened = set()
    # Track the union of property names across all defs of a rel-type
    # so the header reflects every possible property column.
    union_props_per_rel = {}
    for rel_def in schema.get("relationships", []):
        u = union_props_per_rel.setdefault(rel_def["type"], {})
        for p, t in rel_def.get("properties", {}).items():
            u.setdefault(p, t)

    for rel_def in schema.get("relationships", []):
        rel_type = rel_def["type"]
        from_node = nodes_by_id[rel_def["fromId"]]
        to_node = nodes_by_id[rel_def["toId"]]
        from_lbl = primary_label(from_node)
        to_lbl = primary_label(to_node)

        from_ids = id_index.get(from_lbl, [])
        to_ids = id_index.get(to_lbl, [])
        if not from_ids or not to_ids:
            continue

        card = cardinality.get(rel_type, {"kind": "N:N", "avg": 1.0})
        kind = card.get("kind", "N:N")
        avg = card.get("avg", 1.0)

        # Use the UNION of properties for this rel-type as the header.
        all_prop_names = list(union_props_per_rel.get(rel_type, {}).keys())
        # Generators only for the props this particular def declares
        local_props = list(rel_def.get("properties", {}).keys())
        generators = {}
        for p in local_props:
            full_key = f"{rel_type}.{p}"
            if full_key in rel_props:
                generators[p] = make_generator(rel_props[full_key])
            else:
                generators[p] = _default_generator_for(p, rel_def["properties"][p])

        out_path = os.path.join(output_dir, f"rels_{rel_type}.csv")
        # Open in 'w' the first time this rel-type is seen; append after that.
        if rel_type not in opened:
            mode = "w"
            opened.add(rel_type)
            write_header = True
        else:
            mode = "a"
            write_header = False

        count = 0
        with open(out_path, mode, newline="") as fh:
            writer = csv.writer(fh)
            if write_header:
                writer.writerow(["_from_id", "_to_id"] + all_prop_names)

            def _row_for(fid, tid):
                vals = [fid, tid]
                for p in all_prop_names:
                    if p in generators:
                        vals.append(generators[p]())
                    else:
                        vals.append("")  # property not defined for this label pair
                return vals

            if kind == "1:1":
                for fid, tid in zip(from_ids, to_ids):
                    writer.writerow(_row_for(fid, tid))
                    count += 1
            elif kind == "1:N":
                for fid in from_ids:
                    n_targets = max(1, int(random.gauss(avg, max(avg * 0.3, 1))))
                    for _ in range(n_targets):
                        tid = random.choice(to_ids)
                        writer.writerow(_row_for(fid, tid))
                        count += 1
            elif kind == "N:1":
                for tid in to_ids:
                    n_sources = max(1, int(random.gauss(avg, max(avg * 0.3, 1))))
                    for _ in range(n_sources):
                        fid = random.choice(from_ids)
                        writer.writerow(_row_for(fid, tid))
                        count += 1
            else:  # N:N
                for fid in from_ids:
                    n_targets = max(1, int(random.gauss(avg, max(avg * 0.3, 1))))
                    for _ in range(n_targets):
                        tid = random.choice(to_ids)
                        if from_lbl == to_lbl and fid == tid:
                            continue  # skip accidental self-loops
                        writer.writerow(_row_for(fid, tid))
                        count += 1

        rel_counts[rel_type] = rel_counts.get(rel_type, 0) + count
        print(f"  wrote {out_path} (+{count:,} rows; total {rel_counts[rel_type]:,})", file=sys.stderr)

    return rel_counts


