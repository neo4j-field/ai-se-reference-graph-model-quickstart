def compute_node_counts(schema, anchor_label, anchor_count, cardinality):
    """Walk the cardinality table to compute counts for non-anchor labels.

    For each rel in cardinality, derive the size of the smaller side from
    the larger side and the avg fanout. Iterate to fixpoint so chains
    (Customer -> Account -> Transaction) propagate correctly.
    """
    counts = {primary_label(n): None for n in schema.get("nodes", [])}
    counts[anchor_label] = anchor_count

    # Lookup helper
    nodes_by_label = {primary_label(n): n for n in schema.get("nodes", [])}

    # Iterate up to N times to let chains propagate
    for _ in range(len(counts) + 5):
        progress = False
        for rel_type, card in cardinality.items():
            from_lbl = card["from"]
            to_lbl = card["to"]
            avg = card.get("avg", 1.0)
            kind = card.get("kind", "N:N")
            f = counts.get(from_lbl)
            t = counts.get(to_lbl)
            if f is not None and t is None:
                if kind in ("1:N", "N:N"):
                    counts[to_lbl] = max(1, int(round(f * avg)))
                    progress = True
                elif kind == "N:1":
                    counts[to_lbl] = max(1, int(round(f / max(avg, 1.0))))
                    progress = True
                elif kind == "1:1":
                    counts[to_lbl] = f
                    progress = True
            elif t is not None and f is None:
                if kind == "1:N":
                    counts[from_lbl] = max(1, int(round(t / max(avg, 1.0))))
                    progress = True
                elif kind == "N:1":
                    counts[from_lbl] = max(1, int(round(t * avg)))
                    progress = True
                elif kind == "1:1":
                    counts[from_lbl] = t
                    progress = True
        if not progress:
            break

    # Anything still None: default to anchor_count
    for k, v in list(counts.items()):
        if v is None:
            counts[k] = anchor_count

    return counts


