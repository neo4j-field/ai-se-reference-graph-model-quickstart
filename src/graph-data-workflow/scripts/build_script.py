def load_schema(path):
    with open(path) as f:
        raw = json.load(f)
    if "initialGraph" in raw:
        return raw["initialGraph"]
    if "graph" in raw:
        return raw["graph"]
    return raw


def primary_label(node_def):
    labels = node_def.get("labels") or []
    return labels[0] if labels else node_def.get("caption", "Node")


